// A function is like a block of code that performs a specific task.
// You need to call (invoke) the function to make it run and get its output.
// Functions are like reusable tools you can use to perform actions or calculations.

// MDN (Mozilla Developer Network) provides extensive documentation about JavaScript.

// To declare and call a function, follow this syntax:

// Example 1: Declaring and calling a function

// You declare a function named 'demo' using the 'function' keyword.
// Inside the function block, you can write the code that should be executed when the function is called.
function demo() {
    console.log("HELLO WORLD");
    // This is how you declare a function. It prints "HELLO WORLD".
}

// Now you call (invoke) the 'demo' function.
// When this line runs, the code inside the 'demo' function executes.
demo();
// You're calling the function here, and it prints "HELLO WORLD" to the console.

// Example 2: Using parameters and arguments

// You declare a function named 'add' that takes two parameters: 'a' and 'b'.
// Parameters are like placeholders for the values you'll provide when you call the function.
function add(a, b) {
    console.log(a + b);
}

// You call the 'add' function and provide arguments 3 and 4.
// Arguments are the actual values you're passing to the function's parameters.
add(3, 4);
// The function calculates and prints the sum of 3 and 4, which is 7.

// Example 3: Declaring and calling a simple function

// You declare a function named 'fook' without any parameters.
function fook() {
    console.log("gaadide");
    // This function prints "gaadide".
}

// Now you call (invoke) the 'fook' function.
// When this line runs, the code inside the 'fook' function executes.
fook();
// You're calling the function here, and it prints "gaadide" to the console.
