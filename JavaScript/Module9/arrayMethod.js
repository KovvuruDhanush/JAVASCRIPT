// Create an array with some elements.
let arr = [1, 2, 3, 4, 5];

// Check if the variable 'arr' is an array using the Array.isArray() method.
// The Array.isArray() method returns true if the argument is an array, and false otherwise.
let checkForArray = Array.isArray(arr);

// Print the result of the check to the console.
console.log(checkForArray);

// Explanation:

// 1. You create an array called 'arr' with the elements [1, 2, 3, 4, 5].

// 2. You use the Array.isArray() method to check whether the variable 'arr' is an array.
//    - Array.isArray(arr) returns true if 'arr' is an array.
//    - It returns false if 'arr' is not an array.

// 3. The result of the check is stored in the variable 'checkForArray'.

// 4. You print the value of 'checkForArray' to the console using console.log().
//    - If 'arr' is indeed an array, 'checkForArray' will be true.
//    - If 'arr' is not an array, 'checkForArray' will be false.

// The output of the console.log(checkForArray) statement will be:
// true
