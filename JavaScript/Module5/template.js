// Template Literals in JavaScript

// Template literals, also known as template strings, are a way to create strings that allow embedding expressions and multiline strings.

// Example 1: Multiline String
console.log(`Dhanush
is
Awesome`);
// Output:
// Dhanush
// is
// Awesome

// In this example, the template literal preserves the line breaks, creating a multiline string.

// Example 2: Embedding Expressions
let a = 20;
console.log(`My age is ${a}`);
// Output: My age is 20

// Template literals allow you to embed expressions within the string using `${}`.
// This makes it easier to concatenate variables or values into the string without using the '+' operator.

// Using template literals can enhance code readability and maintainability by providing a more concise and natural way to create strings.

// Summary:
// - Template literals are created using backticks (`) instead of single or double quotes.
// - They allow embedding expressions within the string using `${}`.
// - Template literals can contain multiline strings without needing explicit line break characters.
// - Using template literals improves code readability and eliminates the need for explicit string concatenation.

